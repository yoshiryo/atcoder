n = int(input())
s = [input() for _ in range(5)]
num = [[] for _ in range(n)]
judge = [['###', '#.#', '#.#', '#.#', '###'],
['.#.', '##.', '.#.', '.#.', '###'],
['###', '..#', '###', '#..', '###'],
['###', '..#', '###', '..#', '###'],
['#.#', '#.#', '###', '..#', '..#'],
['###', '#..', '###', '..#', '###'],
['###', '#..', '###', '#.#', '###'],
['###', '..#', '..#', '..#', '..#'],
['###', '#.#', '###', '#.#', '###'],
['###', '#.#', '###', '..#', '###']]

for i in range(5):
    t = s[i]
    for j in range(1, 4*n, 4):
        index = j//4
        num[index].append(t[j:j+3])
ans = ""
for i in range(len(num)):
    for j in range(10):
        if num[i] == judge[j]:
            ans += str(j)
print(ans)